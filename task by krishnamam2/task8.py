# Write a Python program to convert temperatures to and from Celsius,Fahrenheit. [ Formula: c = (f-32)(5/9)]

# Celsius to Fahrenheit
c = float(input("Enter temperature in Celsius: "))
f = (c * 9/5) + 32
print("converted temp:",f)

